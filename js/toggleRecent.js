// toggleRecent.js
document.addEventListener("DOMContentLoaded", () => {
  const btn = document.getElementById("toggle-recent-2020");
  const container = document.getElementById("recent-container-2020");
  if (!btn || !container) return; 
  btn.addEventListener("click", () => {
    container.classList.toggle("hidden");
  });
});

